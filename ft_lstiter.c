#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst != NULL)
	{
		f(lst ->content);
		lst = lst -> next;
	}
}

void f(void *s)
{
	char *str = (char *)s;
	str[0] = '0';
}
